
-- Бесконечный список всех целочисленных точек на плоскости

type Point = (Int, Int)

-- Бесконечный список всех целочисленных точек
-- Обход по спирали от центра (0,0)
points :: [Point]
points = (0,0) : concat [ring k | k <- [1..]]
  where
    ring k =
      [(-k, y) | y <- [-(k-1) .. k]] ++      -- левая сторона
      [(x, k) | x <- [-k .. k]] ++            -- верхняя сторона
      [(k, y) | y <- [k-1, k-2 .. -k]] ++     -- правая сторона
      [(x, -k) | x <- [k-1, k-2 .. -k]]       -- нижняя сторона

main :: IO ()
main = print $ take 20 points